        /// <summary>
        /// 随机打乱列表顺序
        /// </summary>
        /// <typeparam name="T">列表元素类型</typeparam>
        /// <param name="list">要打乱的列表</param>
        /// <param name="random">随机数生成器</param>
        private static void Shuffle<T>(List<T> list, System.Random random)
        {
            int n = list.Count;
            while (n > 1)
            {
                n--;
                int k = random.Next(n + 1);
                T value = list[k];
                list[k] = list[n];
                list[n] = value;
            }
        }

