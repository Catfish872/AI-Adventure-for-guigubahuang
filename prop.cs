        // 添加到 Prop 类中的短奇遇专用奖励生成方法
        public static Il2CppSystem.Collections.Generic.List<DataProps.PropsData> GetShortEventRewards(out Dictionary<string, int> rewardInfo)
        {
            // 创建奖励物品列表
            Il2CppSystem.Collections.Generic.List<DataProps.PropsData> rewardItems = new Il2CppSystem.Collections.Generic.List<DataProps.PropsData>();

            // 创建物品名称和数量的字典
            rewardInfo = new Dictionary<string, int>();

            // 创建随机数生成器
            System.Random random = new System.Random();

            // 决定奖励数量（2-3个）
            int rewardCount = random.Next(2, 4); // 2或3个奖励

            // 计算境界范围（用于道具筛选）
            int playerGrade = g.world.playerUnit.data.dynUnitData.GetGrade();
            int minGrade = playerGrade;
            int maxGrade = playerGrade + 1;

            // 获取玩家已有气运列表（用于气运筛选）
            List<int> playerLuckIds = new List<int>();
            foreach (WorldUnitLuckBase luck in g.world.playerUnit.allLuck)
            {
                playerLuckIds.Add(luck.luckConf.id);
            }

            // 为每个奖励位置生成奖励
            for (int i = 0; i < rewardCount; i++)
            {
                double rewardTypeRandom = random.NextDouble();

                if (rewardTypeRandom < 0.2) // 25% 功法
                {
                    try
                    {
                        MartialType martialType = (MartialType)random.Next(1, 6);
                        int baseID = 0;
                        int grade = g.world.playerUnit.data.dynUnitData.GetGrade();
                        int level = 6;

                        RandomGetRewardData randomGetRewardData = new RandomGetRewardData(martialType, level, grade)
                        {
                            baseID = baseID
                        };

                        GameItemRewardData reward = RewardTool.GetReward(randomGetRewardData, null);

                        if (reward != null)
                        {
                            DataProps.PropsData martialProps = RewardTool.GetRewardPropsData(reward);

                            if (martialProps != null)
                            {
                                rewardItems.Add(martialProps);
                                rewardInfo.Add($"{martialProps.propsInfoBase.name}", 1);
                                continue; // 成功生成功法，继续下一个奖励
                            }
                        }
                    }
                    catch (Exception ex)
                    {
                        Debug.Log($"短奇遇功法生成失败: {ex.Message}");
                    }

                    // 功法生成失败，改为生成道具
                    rewardTypeRandom = 0.6; // 强制进入道具分支
                }

                if (rewardTypeRandom >= 0.2 && rewardTypeRandom < 0.35) // 15% 气运
                {
                    List<string> availableLuckKeys = new List<string>();

                    // 根据概率选择气运字典
                    if (random.NextDouble() < 0.9) // luckDict
                    {
                        foreach (var pair in luckDict)
                        {
                            int luckId = int.Parse(pair.Key);
                            if (!playerLuckIds.Contains(luckId))
                            {
                                availableLuckKeys.Add(pair.Key);
                            }
                        }

                        if (availableLuckKeys.Count > 0)
                        {
                            string selectedLuckKey = availableLuckKeys[random.Next(availableLuckKeys.Count)];
                            string luckFullName = luckDict[selectedLuckKey];
                            rewardInfo.Add($"玩家在奇遇中获得的效果：{luckFullName}", 1);
                            continue; // 成功生成气运，继续下一个奖励
                        }
                    }
                    else // luckDict2
                    {
                        foreach (var pair in luckDict2)
                        {
                            int luckId = int.Parse(pair.Key);
                            if (!playerLuckIds.Contains(luckId))
                            {
                                availableLuckKeys.Add(pair.Key);
                            }
                        }

                        if (availableLuckKeys.Count > 0)
                        {
                            string selectedLuckKey = availableLuckKeys[random.Next(availableLuckKeys.Count)];
                            string luckFullName = luckDict2[selectedLuckKey];
                            rewardInfo.Add($"玩家在奇遇中获得的效果：{luckFullName}", 1);
                            continue; // 成功生成气运，继续下一个奖励
                        }
                    }

                    // 气运生成失败（玩家已拥有所有气运），改为生成道具
                    rewardTypeRandom = 0.6; // 强制进入道具分支
                }

                if (rewardTypeRandom >= 0.35) // 50% 道具或灵石
                {
                    // 随机决定是道具还是灵石
                    if (random.NextDouble() < 0.7) // 70% 道具
                    {
                        // 筛选符合境界要求的道具
                        List<string> availableProps1 = new List<string>();
                        List<string> availableProps2 = new List<string>();

                        foreach (var pair in propDict1)
                        {
                            int itemGrade = Tools.ExtractGradeFromName(pair.Value);
                            if (itemGrade == 0 || (itemGrade >= minGrade && itemGrade <= maxGrade))
                            {
                                availableProps1.Add(pair.Key);
                            }
                        }

                        foreach (var pair in propDict2_10)
                        {
                            int itemGrade = Tools.ExtractGradeFromName(pair.Value);
                            if (itemGrade == 0 || (itemGrade >= minGrade && itemGrade <= maxGrade))
                            {
                                availableProps2.Add(pair.Key);
                            }
                        }

                        // 随机选择道具类型
                        if (random.NextDouble() < 0.5 && availableProps1.Count > 0) // propDict1
                        {
                            string selectedKey = availableProps1[random.Next(availableProps1.Count)];
                            int propId = int.Parse(selectedKey);
                            DataProps.PropsData propsData = DataProps.PropsData.NewProps(propId, 1);
                            rewardItems.Add(propsData);
                            string originalName = Tools.GetOriginalItemName(propDict1[selectedKey]);
                            rewardInfo.Add(originalName, 1);
                        }
                        else if (availableProps2.Count > 0) // propDict2_10
                        {
                            string selectedKey = availableProps2[random.Next(availableProps2.Count)];
                            int propId = int.Parse(selectedKey);
                            int amount = random.Next(2, 6); // 短奇遇给少一点数量
                            DataProps.PropsData propsData = DataProps.PropsData.NewProps(propId, amount);
                            rewardItems.Add(propsData);
                            string originalName = Tools.GetOriginalItemName(propDict2_10[selectedKey]);
                            rewardInfo.Add(originalName, amount);
                        }
                        else
                        {
                            // 没有可用道具，生成灵石
                            GenerateSpirit(rewardItems, rewardInfo, playerGrade, random);
                        }
                    }
                    else // 30% 灵石
                    {
                        GenerateSpirit(rewardItems, rewardInfo, playerGrade, random);
                    }
                }
            }

            return rewardItems;
        }

        // 辅助方法：生成灵石
        private static void GenerateSpirit(Il2CppSystem.Collections.Generic.List<DataProps.PropsData> rewardItems,
            Dictionary<string, int> rewardInfo, int playerGrade, System.Random random)
        {
            int baseAmount = playerGrade * 3000; 
            int variation = (int)(baseAmount * (random.NextDouble() - 0.5));
            int amount = Math.Max(10, baseAmount + variation);
            DataProps.PropsData spiritualStone = DataProps.PropsData.NewProps(10001, amount);
            rewardItems.Add(spiritualStone);
            rewardInfo.Add("灵石", amount);
        }

        public static string GetPlayerCompleteInfo(WorldUnitBase unit, WorldUnitBase toUnit = null, int maxLogCount = 5)
        {
            if (unit == null) return "无效NPC";

            // 如果未提供参照单位，默认使用玩家
            if (toUnit == null)
            {
                toUnit = g.world.playerUnit;
            }

            StringBuilder info = new StringBuilder();

            try
            {
                // 获取基本信息
                string basicInfo = GetNPCInfoString(unit, toUnit);
                info.AppendLine("【基本资料】");
                info.AppendLine(basicInfo);

                // 获取个人经历
                string personalHistory = GetNPCPersonalHistory(unit, maxLogCount);
                info.AppendLine("【个人经历】");
                info.AppendLine(personalHistory);
            }
            catch (Exception ex)
            {
                Debug.Log($"获取NPC完整信息时出错: {ex.Message}");
                return $"获取NPC信息失败: {ex.Message}";
            }

            return info.ToString();
        }
